static int search_database(float f)
{
    int min, max, mid;

    min = 0; 
    max = sizeof(s_database) / sizeof(s_database[0]) - 1;
    while (min <= max) {
        mid = (min + max) >> 1;
        if (f == s_database[mid]) {
            return mid + 1;
        } else if (f < s_database[mid]) {
            max = mid - 1;
        } else {
            min = mid + 1;
        }
    }
    return min + 1;
}

